package ex2;

    public class Arithmetics {
        public static void main(String[] args){
            int num = 100;
            int factor = 20;
            int sum = 0;

            sum = num + factor;
            System.out.println("Summa " + sum);

            sum = num - factor;
            System.out.println("Vichitanie " + sum);

            sum = num * factor;
            System.out.println("Umnozhenie " + sum);

            sum = num / factor;
            System.out.println("Delenie " + sum);
        }

    }
